    <script>
        const lightbox = document.getElementById('lightbox');
        const lightboxImg = document.getElementById('lightbox-img');
        const lightboxVideo = document.getElementById('lightbox-video');
        const closeBtn = document.getElementById('close');

        document.querySelectorAll('.gallery-item img, .gallery-item video').forEach(item => {
            item.addEventListener('click', () => {
                if (item.tagName === 'IMG') {
                    lightboxImg.src = item.src;
                    lightboxImg.style.display = 'block';
                    lightboxVideo.style.display = 'none';
                    closeBtn.style.display = 'block'; // إظهار زر الإغلاق للصور
                } else if (item.tagName === 'VIDEO') {
                    lightboxVideo.querySelector('source').src = item.querySelector('source').src;
                    lightboxVideo.load();
                    lightboxVideo.style.display = 'block';
                    lightboxImg.style.display = 'none';
                    closeBtn.style.display = 'none'; // إخفاء زر الإغلاق للفيديوهات
                }
                lightbox.style.display = 'flex';
            });
        });

        closeBtn.addEventListener('click', () => {
            lightbox.style.display = 'none';
            if (!lightboxVideo.paused) {
                lightboxVideo.pause();
            }
            lightboxVideo.currentTime = 0;
        });

        lightbox.addEventListener('click', (e) => {
            if (e.target === lightbox) {
                lightbox.style.display = 'none';
                if (!lightboxVideo.paused) {
                    lightboxVideo.pause();
                }
                lightboxVideo.currentTime = 0;
            }
        });
        
    </script>

  

